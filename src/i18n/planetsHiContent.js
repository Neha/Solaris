/** Merge Hindi fields over planet object for display when locale is Hindi */
export function getLocalizedPlanetBody(englishName, isSun, basePlanet) {
  if (isSun) {
    return { ...basePlanet, ...SUN_HI }
  }
  const hi = PLANETS_HI[englishName]
  if (!hi) return basePlanet
  return { ...basePlanet, ...hi }
}
